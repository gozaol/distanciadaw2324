/**
 *
 * Esta función realiza la multiplicación de dos números que se pasan por parámetros y devuelve el resultado de la misma.
 *
 * @param int $a El primer número.
 * @param int $b El segundo número.
 * @return int La multiplicación de los dos números.
 *
 */

function multiplicar($a, $b) {
    return $a * $b;
}

/**
 *
 * Esta función realiza la división de dos números que se pasan por parámetros y devuelve el resultado de la misma.
 *
 * @param int $a El primer número.
 * @param int $b El segundo número.
 * @return int La división de los dos números.
 * 
 */

function dividir($a, $b) {
    return $a / $b;
}


